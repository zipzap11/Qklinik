    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
        // TODO add your handling code here:
          int uangPasien = Integer.parseInt(totalPembayaran.getText());
          Pembayaran pembayaran = admin.getPembayaran();
          pembayaran.setUangPembayaran(uangPasien);
          int uangKembalian = pembayaran.getKembalian();
          if (uangKembalian < 0) {
              JOptionPane.showMessageDialog(this, "Uang pembayaran tidak cukup.");
              return;
          }
          
          admin.incrementBerobat();
          JOptionPane.showMessageDialog(this, "Uang Kembalian : Rp. " + uangKembalian);
          kembalian.setText("Rp. " + uangKembalian);
    }//GEN-LAST:event_btnBayarActionPerformed

