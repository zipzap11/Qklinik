    private void btnTambahObatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahObatActionPerformed
        // TODO add your handling code here:
        String kode = kodeObat.getText();
        String nama = namaObat.getText();
        String harga = hargaObat.getText();
        boolean isSuccess = admin.tambahObatBaru(kode, nama, harga, this);
        if (isSuccess) {
            JOptionPane.showMessageDialog(this, "Berhasil menambahkan data obat.");
            tampilkanDataObat();
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menambahkan data obat.");
        }
        resetInputDataObat();
    }//GEN-LAST:event_btnTambahObatActionPerformed

