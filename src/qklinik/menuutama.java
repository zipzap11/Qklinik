    private void btnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCetakActionPerformed
        try {
            // TODO add your handling code here:
            Pembayaran pembayaran = admin.getPembayaran();
            Pasien pasien = pembayaran.getPasien();
            
            if (pasien == null) {
                return;
            }
            String namaFile = "d:Nota" + pasien.getNama() + pasien.getJmlBerobat() + ".txt";
            File file = new File(namaFile);
            if (file.createNewFile()) {
                BufferedWriter writer = new BufferedWriter(new FileWriter(file));
                String dataNama = "Nama : " + pasien.getNama() + "\n";
                String dataDokter = "Dokter : " + pembayaran.getDokter().getNama() + "\n";
                writer.write(dataNama);
                writer.write(dataDokter);
                writer.newLine();
                writer.write("Daftar Obat \n--------------------- \n");
                Obat [] daftarObat = pembayaran.getListObat();
                for (int i = 0; i < daftarObat.length; i++) {
                    if (daftarObat[i] == null) continue;
                    writer.write(daftarObat[i].getKode() + " - " + daftarObat[i].getNama() + " - " + daftarObat[i].getHarga());
                    writer.newLine();
                }
                writer.write("---------------------\n");
                writer.newLine();
                writer.write("Total harga \tRp. " + pembayaran.getTotalHarga() + "\n");
                writer.write("Kembalian \tRp. " + pembayaran.getKembalian());
                writer.flush();
                JOptionPane.showMessageDialog(this, "Berhasil Mencetak. (" + namaFile + ")");
            } else {
                JOptionPane.showMessageDialog(this, "Gagal Mencetak.");

            }
        } catch (IOException ex) {
            Logger.getLogger(menuutama.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        resetPanelPembayaran();
    }//GEN-LAST:event_btnCetakActionPerformed

